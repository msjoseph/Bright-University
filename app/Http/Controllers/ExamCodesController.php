    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $permitted_chars = '0123456789abcdefghijklmnopqrstuvwxyzABCDEFGHIJKLMNOPQRSTUVWXYZ';

        function generate_string($input, $strength = 16) {
            $input_length = strlen($input);
            $random_string = '';
            for($i = 0; $i < $strength; $i++) {
                $random_character = $input[mt_rand(0, $input_length - 1)];
                $random_string .= $random_character;
            }

            return $random_string;
        }


        $students = Student::all();
        foreach ($students as $student)
        {
            if ($student->debit >= $student->credit)
            {
                $examcode = new ExamCode;
                $examcode->student_id = $student->id;
                $gen_code = generate_string($permitted_chars, 7).date('Y');
                $examcode->code = $gen_code;
                $examcode->save();
            }

        }

        return redirect('/finance')->with('success', 'Examination codes generated');

    }

